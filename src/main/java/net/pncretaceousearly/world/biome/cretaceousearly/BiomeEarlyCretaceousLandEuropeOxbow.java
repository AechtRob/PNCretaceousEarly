		public WorldGenAbstractTree getRandomTreeFeature(Random rand)
		{
			if (rand.nextInt(12) != 0) {
				return CYCADOPTERIS_TREE;
			}
			if (rand.nextInt(30) == 0) {
				return PAGIOPHYLLUM_TREE;
			}
			int i = rand.nextInt(3);
			switch (i) {
				case 0: default:
					return CEDAR_TREE;

				case 1:
					return YEW_TREE;

				case 2:
					return PINE_TREE;
			}
		}

