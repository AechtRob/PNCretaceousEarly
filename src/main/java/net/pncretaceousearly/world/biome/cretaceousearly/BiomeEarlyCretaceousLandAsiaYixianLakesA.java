		public WorldGenAbstractTree getRandomTreeFeature(Random rand)
		{
			int i = rand.nextInt(100);
			if (i <= 2) {
				return PAGIO_TREE;
			}
			else if (i <= 5) {
				return YEW_TREE;
			}
			else if (i <= 15) {
				return TREEFERN_TREE;
			}
			else if (i <= 28) {
				return BRACHY_TREE;
			}
			else if (i <= 40) {
				return ELATO_TREE;
			}
			else if (i <= 50) {
				return CYPRESS_TREE;
			}
			else if (i <= 70) {
				if (rand.nextInt(2) == 0) {
					return TREEFERN_TREE;
				}
				return LARCH_TREE;
			}
			else if (i <= 80) {
				if (rand.nextInt(6) == 0) {
					return CUNNING_TREE;
				}
				return PINE_TREE;
			}
			else if (i <= 70) {
				return SPHENO_TREE;
			}
			else {
				return CEPHA_TREE;
			}
		}

