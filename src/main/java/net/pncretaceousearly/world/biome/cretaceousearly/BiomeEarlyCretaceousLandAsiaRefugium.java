		public WorldGenAbstractTree getRandomTreeFeature(Random rand)
		{
			if (rand.nextInt(4) != 0) {
				return NULL_TREE;
			}
			int i = rand.nextInt(11);
			switch (i) {
				case 0: default:
					return GINKGO_TREE;
				case 1:
					return PINE_TREE;
				case 2:
					return PINE_TREE;
				case 3:
					return PODOCARP_TREE;
				case 4:
					return PODOCARP_TREE;
				case 5:
					return REDWOOD_TREE;
				case 6:
					return ELATOCLADUS_TREE;
				case 7:
					return SILVERTREEFERN_TREE;
				case 8:
					return DICKSONIA_TREE;
				case 9:
					return PINE_TREE;
				case 10:
					return ELATOCLADUS_TREE;
			}
		}

